public class ReporteTresUltimosDias {
    
    double suma;
    String dia;

    public ReporteTresUltimosDias() {
    }

    public ReporteTresUltimosDias(double suma, String dia) {
        this.suma = suma;
        this.dia = dia;
    }

    public double getSuma() {
        return suma;
    }

    public void setSuma(double suma) {
        this.suma = suma;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }

  
    
}
